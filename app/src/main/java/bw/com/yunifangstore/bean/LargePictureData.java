public class LargePictureData {
    /**
     * url : http://image.hmeili.com/yunifang/images/goods/ad6/16120519446329748741281252.jpg
     * width : 790
     * height : 640
     */

    private String url;
    private String width;
    private String height;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }


}
